  800 E=ZERO
      GO TO 850
  810 WRITE(LP,830)DICT(IENT),DICT(5),N
      GO TO 840
  820 WRITE(LP,830)DICT(IENT),DICT(6),L,DICT(8 )
  830 FORMAT(' ERROR RETURN FROM',2A8,I5,1X,A8)
  840 E=-TWO
  850 RETURN
